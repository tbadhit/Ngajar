  // OR = Jika salah satu perbandingannya bernilai true maka outpurnya akan true
  var sameOrNot = 1 == 2 || 5 <= 10;
  print(sameOrNot);

  // AND = jika salah satu perbandingannya bernilai false maka outputnya akan false
